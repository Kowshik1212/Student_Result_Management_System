    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String rollNo = roll.getText();
        String courseName = (String)course.getSelectedItem();
        String branchName = (String)branch.getSelectedItem();
        String name = Name.getText();
        String Gender = (String)gender.getSelectedItem();
        String fatherName = father.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Kowshik@2121");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno = '"+rollNo+"'");
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"This Roll No is already registered!");
                setVisible(false);
                new adminPortal().setVisible(true);
            }
            else
            {
                st.executeUpdate("insert into student values ('"+rollNo+"','"+courseName+"','"+branchName+"','"+name+"','"+Gender+"','"+fatherName+"')");
                JOptionPane.showMessageDialog(null,"Data Saved Successfully");
                setVisible(false);
                new adminPortal().setVisible(true);
            }
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

